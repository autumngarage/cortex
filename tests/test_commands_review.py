def test_review_renders_cited_contradiction_finding_fully_offline(
    offline_env: None,
    project: Path,
    decisions_fixture_path: Path,
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    monkeypatch.setenv(review.MODEL_FIXTURES_ENV_VAR, str(_REVIEW_FIXTURES_DIR))
    diff_path = _write_diff(tmp_path, CONTRADICTION_DIFF)
    result = CliRunner().invoke(
        cli,
        [
            "review",
            "--diff",
            str(diff_path),
            "--decisions-fixture",
            str(decisions_fixture_path),
            "--path",
            str(project),
        ],
    )
    assert result.exit_code == 0, _combined_output(result)
    out = result.output
    # The finding block: class, tier glyph, summary, citation, repair.
    assert "1 advisory finding(s)" in out
    assert "contradicts-prior-decision" in out
    assert "▲ advisory -> advisory_comment" in out
    assert "fixed 0.5s delay" in out
    assert f"citation: {_ADR_PERMALINK}" in out
    assert _ADR_EXCERPT in out
    assert "suggested repair:" in out
    # Suppression/omission accounting stays visible (cortex#331 discipline).
    assert "suppressed below floor: 0" in out
    assert "status_filtered=1" in out
    assert "suppressed_below_floor=1" in out
    assert "over_budget=0" in out
    # The replay key line carries the full key material.
    assert f"replay-key: model={PLAYBACK_MODEL_ID}" in out
    assert "retrieval=fixture-local-structural-v1" in out
    assert f"prompt={review.REVIEW_PROMPT_VERSION}" in out
    assert "run=cortex-review-" in out


def test_review_exit_code_never_branches_on_findings(
    offline_env: None,
    project: Path,
    decisions_fixture_path: Path,
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """Advisory invariant (#375): findings and no-findings both exit 0."""

    monkeypatch.setenv(review.MODEL_FIXTURES_ENV_VAR, str(_REVIEW_FIXTURES_DIR))
    exit_codes: dict[str, int] = {}
    outputs: dict[str, str] = {}
    for name, diff_text in (("findings", CONTRADICTION_DIFF), ("clean", BENIGN_DIFF)):
        diff_path = _write_diff(tmp_path, diff_text, name=f"{name}.diff")
        result = CliRunner().invoke(
            cli,
            [
                "review",
                "--diff",
                str(diff_path),
                "--decisions-fixture",
                str(decisions_fixture_path),
                "--path",
                str(project),
            ],
        )
        exit_codes[name] = result.exit_code
        outputs[name] = _combined_output(result)
    assert "state: findings_emitted" in outputs["findings"]
    assert "state: no_findings" in outputs["clean"]
    assert "0 advisory finding(s)" in outputs["clean"]
    assert exit_codes == {"findings": 0, "clean": 0}


