class _CapturingEvaluateModel:
    """Scripted EvaluateModel: binds every result to its request, no live calls."""

    def __init__(
        self,
        findings_for: Callable[[EvaluateRequest], tuple[FindingDraft, ...]],
        *,
        model_id: str = PLAYBACK_MODEL_ID,
    ) -> None:
        self._findings_for = findings_for
        self._model_id = model_id
        self.requests: list[EvaluateRequest] = []
        self.results: list[EvaluateResult] = []

    def evaluate(self, request: EvaluateRequest) -> EvaluateResult:
        result = EvaluateResult(
            findings=self._findings_for(request),
            model_id=self._model_id,
            prompt_version=request.prompt_version,
            input_hash=request.input_hash,
        )
        self.requests.append(request)
        self.results.append(result)
        return result


def _contradiction_findings(request: EvaluateRequest) -> tuple[FindingDraft, ...]:
    candidate = next(
        item
        for item in request.candidate_pack.candidates
        if "exponential backoff" in item.decision_text
    )
    return (
        FindingDraft(
            finding_class=FindingClass.CONTRADICTS_PRIOR_DECISION,
            decision_node_id=candidate.decision_node_id,
            cited_span_hashes=tuple(span.span_hash for span in candidate.cited_spans),
            summary="The diff replaces exponential backoff with a fixed 0.5s delay.",
            confidence_label="advisory",
            suggested_repair=(
                "Keep exponential backoff (2.0 ** attempt) or supersede the "
                "decision before landing a fixed delay."
            ),
        ),
    )


def _no_findings(request: EvaluateRequest) -> tuple[FindingDraft, ...]:
    _ = request
    return ()


def _generate_recordings_json() -> str:
    """Build the committed recorded-response fixture through the one code path.

    ``review.evaluate_review`` constructs the exact ``EvaluateRequest`` the
    CLI will build (same pack builder, same prompt version, same budget), so
    the recorded ``input_hash`` can never drift from the command.
    """

    fixture = _decisions_fixture()
    results: list[EvaluateResult] = []
    scripted: tuple[
        tuple[str, Callable[[EvaluateRequest], tuple[FindingDraft, ...]]], ...
    ] = (
        (CONTRADICTION_DIFF, _contradiction_findings),
        (BENIGN_DIFF, _no_findings),
    )
    for diff_text, findings_for in scripted:
        pack = review.build_offline_candidate_pack(fixture, diff_text)
        capture = _CapturingEvaluateModel(findings_for)
        review.evaluate_review(
            pack=pack,
            diff_text=diff_text,
            model=capture,
            token_budget=review.DEFAULT_REVIEW_TOKEN_BUDGET,
            tenant_id=_TENANT,
            source_id=_SOURCE,
        )
        results.extend(capture.results)
    return RecordedResponseAdapter(evaluate_results=results).to_canonical_json()


def _combined_output(result: object) -> str:
    output = getattr(result, "output", "") or ""
    stderr = getattr(result, "stderr", "") or ""
    return output + stderr


