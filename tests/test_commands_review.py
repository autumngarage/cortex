def _init_git_repo(root: Path) -> None:
    def _git(*args: str) -> None:
        subprocess.run(
            [
                "git",
                "-c",
                "user.email=review-test@example.com",
                "-c",
                "user.name=review-test",
                *args,
            ],
            cwd=root,
            check=True,
            capture_output=True,
            text=True,
        )

    _git("init", "-q")
    (root / "tracked.py").write_text("ORIGINAL = 1\n", encoding="utf-8")
    _git("add", "tracked.py")
    _git("commit", "-q", "-m", "initial")


def test_resolve_diff_text_staged_reads_the_index(tmp_path: Path) -> None:
    repo = tmp_path / "repo"
    repo.mkdir()
    _init_git_repo(repo)
    (repo / "tracked.py").write_text("ORIGINAL = 1\nADDED = 2\n", encoding="utf-8")
    subprocess.run(["git", "add", "tracked.py"], cwd=repo, check=True, capture_output=True)
    diff_text = review.resolve_diff_text(repo, staged=True)
    assert "+ADDED = 2" in diff_text


