# A fake installation client: serves a canned repo, records every call
# ---------------------------------------------------------------------------


class FakeInstallationClient:
    """A canned GitHub repo served entirely from memory; records writes.

    Files and directory listings are keyed by ``(path, ref)`` so the test can
    assert the reviewer fetched the *base* SHA. Posting and updating record
    onto ``posted`` / ``updated`` so a dry-run can be asserted to have made
    zero writes, and an idempotent re-run can be asserted to update not
    duplicate.
    """

    def __init__(
        self,
        *,
        files: Mapping[tuple[str, str], str],
        directories: Mapping[tuple[str, str], tuple[DirectoryEntry, ...]],
        diff: str,
    ) -> None:
        self._files = dict(files)
        self._directories = dict(directories)
        self._diff = diff
        self.comments: list[dict[str, Any]] = []
        self.posted: list[dict[str, Any]] = []
        self.updated: list[dict[str, Any]] = []
        self.diff_calls: list[tuple[str, str, int]] = []
        self._next_comment_id = 1000

    def get_pull_request_diff(self, owner: str, repo: str, pr_number: int) -> str:
        self.diff_calls.append((owner, repo, pr_number))
        return self._diff

    def get_file_contents(self, owner: str, repo: str, path: str, ref: str) -> bytes | None:
        content = self._files.get((path, ref))
        if content is None:
            return None
        return content.encode("utf-8")

    def list_directory(
        self, owner: str, repo: str, path: str, ref: str
    ) -> tuple[DirectoryEntry, ...]:
        return self._directories.get((path, ref), ())

    def list_pull_request_files(
        self, owner: str, repo: str, pr_number: int
    ) -> tuple[ChangedFile, ...]:  # pragma: no cover - reviewer uses the diff
        return ()

    def list_issue_comments(
        self, owner: str, repo: str, issue_number: int
    ) -> tuple[Mapping[str, Any], ...]:
        return tuple(self.comments)

    def post_issue_comment(
        self, owner: str, repo: str, issue_number: int, body: str
    ) -> Mapping[str, Any]:
        comment_id = self._next_comment_id
        self._next_comment_id += 1
        record = {
            "id": comment_id,
            "html_url": f"https://github.com/{owner}/{repo}/pull/{issue_number}#c{comment_id}",
            "body": body,
        }
        self.comments.append(record)
        self.posted.append(record)
        return {"id": record["id"], "html_url": record["html_url"]}

    def update_issue_comment(
        self, owner: str, repo: str, comment_id: int, body: str
    ) -> Mapping[str, Any]:
        for record in self.comments:
            if record["id"] == comment_id:
                record["body"] = body
                self.updated.append(record)
                return {"id": comment_id, "html_url": record["html_url"]}
        raise GithubApiError(f"no comment {comment_id} to update")


def _full_repo_client(diff: str = CONTRADICTION_DIFF) -> FakeInstallationClient:
    doctrine_path = ".cortex/doctrine/0001-retry.md"
    return FakeInstallationClient(
        files={
            ("CLAUDE.md", _BASE_SHA): CLAUDE_MD,
            (doctrine_path, _BASE_SHA): DOCTRINE_MD,
        },
        directories={
            (".cortex/doctrine", _BASE_SHA): (DirectoryEntry(path=doctrine_path, type="file"),),
            (".cortex/plans", _BASE_SHA): (),
        },
        diff=diff,
    )


def _empty_repo_client(diff: str = CONTRADICTION_DIFF) -> FakeInstallationClient:
    """A repo with no CLAUDE.md, no AGENTS.md, no .cortex/ — the degraded case."""

    return FakeInstallationClient(files={}, directories={}, diff=diff)


