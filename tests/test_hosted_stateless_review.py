def test_dry_run_renders_cited_contradiction_and_posts_nothing() -> None:
    client = _full_repo_client(CONTRADICTION_DIFF)
    result = run_stateless_review(
        _payload(),
        client=client,
        model=_RoutedRecordedModel(),
        config=ReviewHandlerConfig(dry_run=True),
        now=lambda: _FIXED_TIME,
    )
    assert result.dry_run is True
    assert result.posted is False
    assert result.finding_count == 1
    assert result.no_decisions is False
    body = result.comment_body
    assert "Contradicts a prior decision" in body
    assert "import sentinel" in body
    # The cited decision resolves to a base-SHA blob permalink (the repo is
    # the store; the citation points back at the customer's own file).
    assert f"blob/{_BASE_SHA}/CLAUDE.md" in body
    # The stable marker carries this PR + head SHA so the poster can update it.
    marker = extract_marker(body)
    assert marker is not None
    assert marker.pr_number == _PR_NUMBER
    assert marker.head_sha == _HEAD_SHA
    # Dry-run made zero writes to GitHub.
    assert client.posted == []
    assert client.updated == []
    # And it fetched the decision baseline at the BASE sha, the diff once.
    assert client.diff_calls == [(_OWNER, _REPO, _PR_NUMBER)]


def test_dry_run_no_findings_on_benign_diff() -> None:
    client = _full_repo_client(BENIGN_DIFF)
    result = run_stateless_review(
        _payload(),
        client=client,
        model=_RoutedRecordedModel(),
        config=ReviewHandlerConfig(dry_run=True),
        now=lambda: _FIXED_TIME,
    )
    assert result.finding_count == 0
    assert result.no_decisions is False
    assert "No contradictions found" in result.comment_body
    assert client.posted == []


