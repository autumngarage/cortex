def _evaluate_full_repo(
    diff: str,
    findings_for: Callable[[EvaluateRequest], tuple[FindingDraft, ...]],
    *,
    token_budget: int = 8000,
) -> Any:
    """Run evaluate_review over the canned repo with a scripted model."""

    from cortex.commands.review import evaluate_review

    event = parse_pull_request_payload(_payload())
    client = _full_repo_client(diff)
    sources = stateless_review.fetch_decision_sources(client, event, ReviewHandlerConfig())
    decisions = stateless_review.derive_fixture_decisions(
        sources, event, source_timestamp=_FIXED_TIME
    )
    pack = stateless_review.build_review_pack(decisions, event, diff)
    return evaluate_review(
        pack=pack,
        diff_text=diff,
        model=_CapturingEvaluateModel(findings_for),
        token_budget=token_budget,
        tenant_id=event.tenant_id,
        source_id=event.source_id,
    )


def test_from_outcome_maps_shadow_captures_to_unconfirmed_twin_count() -> None:
    from cortex.hosted.github_comment import ReviewAccounting

    def _shadow(request: EvaluateRequest) -> tuple[FindingDraft, ...]:
        candidate = next(
            item
            for item in request.candidate_pack.candidates
            if "file contract" in item.decision_text
        )
        return (
            FindingDraft(
                finding_class=FindingClass.CITES_MISSING_PATH,
                decision_node_id=candidate.decision_node_id,
                cited_span_hashes=tuple(span.span_hash for span in candidate.cited_spans),
                summary=(
                    "The diff imports src/widgets/missing_helper.py, which does "
                    "not exist in the changed surface."
                ),
                confidence_label="advisory",
            ),
        )

    outcome = _evaluate_full_repo(CONTRADICTION_DIFF, _shadow)
    accounting = ReviewAccounting.from_outcome(outcome)
    assert accounting.unconfirmed_twin_count == outcome.shadow_finding_count
    assert accounting.unconfirmed_twin_count >= 1
    assert accounting.suppressed_below_floor == outcome.suppressed_below_floor


def test_from_outcome_surfaces_model_omitted_as_degraded_reason() -> None:
    from cortex.hosted.github_comment import ReviewAccounting

    # A token budget of 1 forces the single packed decision to be omitted for
    # budget; from_outcome carries that count into the disclosure.
    outcome = _evaluate_full_repo(CONTRADICTION_DIFF, _no_findings, token_budget=1)
    accounting = ReviewAccounting.from_outcome(outcome)
    assert accounting.omitted_for_budget == outcome.omitted_for_budget
    assert accounting.omitted_for_budget >= 1


