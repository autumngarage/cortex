def test_repo_scoped_ids_are_deterministic_and_repo_specific() -> None:
    a = PullRequestEvent(
        installation_id="1",
        owner="acme",
        repo="widgets",
        pr_number=1,
        base_sha="a" * 7,
        head_sha="b" * 7,
    )
    a_again = PullRequestEvent(
        installation_id="9",
        owner="acme",
        repo="widgets",
        pr_number=2,
        base_sha="c" * 7,
        head_sha="d" * 7,
    )
    b = PullRequestEvent(
        installation_id="1",
        owner="acme",
        repo="gadgets",
        pr_number=1,
        base_sha="a" * 7,
        head_sha="b" * 7,
    )
    # Same repo -> same tenant/source UUID regardless of PR/installation.
    assert a.tenant_id == a_again.tenant_id
    assert a.source_id == a_again.source_id
    # Different repo -> different identity.
    assert a.tenant_id != b.tenant_id
