def test_decision_sources_are_fetched_at_base_sha_only() -> None:
    """A decision file present only at HEAD is not fetched (base is the baseline)."""

    doctrine_path = ".cortex/doctrine/0001-retry.md"
    client = FakeInstallationClient(
        files={
            ("CLAUDE.md", _BASE_SHA): CLAUDE_MD,
            (doctrine_path, _BASE_SHA): DOCTRINE_MD,
            # A file only at HEAD must be invisible to the base-SHA fetch.
            ("CLAUDE.md", _HEAD_SHA): "# head only\n",
        },
        directories={
            (".cortex/doctrine", _BASE_SHA): (DirectoryEntry(path=doctrine_path, type="file"),),
            (".cortex/plans", _BASE_SHA): (),
        },
        diff=CONTRADICTION_DIFF,
    )
