def test_review_path_imports_no_database_connection(monkeypatch: pytest.MonkeyPatch) -> None:
    """Invariant: the stateless review never opens a database connection.

    ``cortex.hosted.db.connect`` is the one door to Postgres; if the review
    path called it, this would fire. The whole pipeline runs with it
    booby-trapped and still produces the cited comment — proving the review is
    DB-free (the repo is the store).
    """

    import cortex.hosted.db as db

    def _explode(*_args: Any, **_kwargs: Any) -> Any:
        raise AssertionError("the stateless review path must not touch the database")

    monkeypatch.setattr(db, "connect", _explode)
    client = _full_repo_client(CONTRADICTION_DIFF)
    result = run_stateless_review(
        _payload(),
        client=client,
        model=_RoutedRecordedModel(),
        config=ReviewHandlerConfig(dry_run=True),
        now=lambda: _FIXED_TIME,
    )
    assert result.finding_count == 1


