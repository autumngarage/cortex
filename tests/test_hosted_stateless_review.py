def _payload(diff_marker: str = "pull_request") -> dict[str, Any]:
    _ = diff_marker
    return {
        "event": "pull_request",
        "body": {
            "action": "opened",
            "installation": {"id": int(_INSTALLATION_ID)},
            "repository": {"name": _REPO, "owner": {"login": _OWNER}},
            "pull_request": {
                "number": _PR_NUMBER,
                "base": {"sha": _BASE_SHA},
                "head": {"sha": _HEAD_SHA},
            },
        },
    }


def _generate_recordings_json() -> str:
    """Build the committed recording through ``run_stateless_review`` itself.

    The exact pack/diff/prompt/budget the handler builds are the ones the
    capturing model sees, so the recorded ``input_hash`` is byte-identical to
    what playback will key on. Both the contradiction and benign diffs are
    recorded so the offline suite can exercise findings and no-findings.
    """

    results: list[EvaluateResult] = []
    scripted: tuple[tuple[str, Callable[[EvaluateRequest], tuple[FindingDraft, ...]]], ...] = (
        (CONTRADICTION_DIFF, _contradiction_findings),
        (BENIGN_DIFF, _no_findings),
        (UNRELATED_DIFF, _no_findings),
    )
    for diff_text, findings_for in scripted:
        client = _full_repo_client(diff_text)
        capture = _CapturingEvaluateModel(findings_for)
        run_stateless_review(
            _payload(),
            client=client,
            model=capture,
            config=ReviewHandlerConfig(dry_run=True),
            now=lambda: _FIXED_TIME,
        )
        results.extend(capture.results)
    return RecordedResponseAdapter(evaluate_results=results).to_canonical_json()


def test_committed_recordings_match_generator() -> None:
    """Drift guard: the committed fixture is byte-identical to a regeneration."""

    assert _RECORDINGS_PATH.is_file(), (
        f"missing committed recording {_RECORDINGS_PATH}; regenerate it with "
        "tests.test_hosted_stateless_review._generate_recordings_json()"
    )
    committed = _RECORDINGS_PATH.read_text(encoding="utf-8")
    assert committed == _generate_recordings_json()


