class _CapturingEvaluateModel:
    """Scripted EvaluateModel binding each result to its request; no live call."""

    def __init__(
        self,
        findings_for: Callable[[EvaluateRequest], tuple[FindingDraft, ...]],
        *,
        model_id: str = PLAYBACK_MODEL_ID,
    ) -> None:
        self._findings_for = findings_for
        self._model_id = model_id
        self.results: list[EvaluateResult] = []

    def evaluate(self, request: EvaluateRequest) -> EvaluateResult:
        result = EvaluateResult(
            findings=self._findings_for(request),
            model_id=self._model_id,
            prompt_version=request.prompt_version,
            input_hash=request.input_hash,
        )
        self.results.append(result)
        return result


def _contradiction_findings(request: EvaluateRequest) -> tuple[FindingDraft, ...]:
    candidate = next(
        item for item in request.candidate_pack.candidates if "file contract" in item.decision_text
    )
    return (
        FindingDraft(
            finding_class=FindingClass.CONTRADICTS_PRIOR_DECISION,
            decision_node_id=candidate.decision_node_id,
            cited_span_hashes=tuple(span.span_hash for span in candidate.cited_spans),
            summary=(
                "The diff adds `import sentinel` to src/widgets/core.py, directly "
                "reversing the confirmed compose-by-file-contract decision."
            ),
            confidence_label="advisory",
            suggested_repair=(
                "Remove the `import sentinel` dependency and integrate by file "
                "contract, or supersede the decision before landing this."
            ),
        ),
    )


def _no_findings(request: EvaluateRequest) -> tuple[FindingDraft, ...]:
    _ = request
    return ()


class _RoutedRecordedModel:
    """Wraps the committed RecordedResponseAdapter as an EvaluateModel.

    The adapter plays back by ``(task, input_hash)`` and never falls back to a
    live call; this thin shim presents the ``evaluate(request)`` protocol the
    handler injects, with the route identity the recording was made under.
    """

    def __init__(self) -> None:
        self._adapter = RecordedResponseAdapter.from_json(
            _RECORDINGS_PATH.read_text(encoding="utf-8")
        )

    def evaluate(self, request: EvaluateRequest) -> EvaluateResult:
        from cortex.hosted.routing import RouteConfig, TaskKind

        route = RouteConfig(
            task_kind=TaskKind.EVALUATE,
            model_id=PLAYBACK_MODEL_ID,
            adapter_id="recorded-responses",
        )
        outcome = self._adapter.run_evaluate(request, route)
        assert isinstance(outcome.result, EvaluateResult)
        return outcome.result


